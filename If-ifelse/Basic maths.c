// Simple maths 
#include<stdio.h>
#include<math.h>
int main(){
   int a = 4;
   int b = 6;
   printf("The Add is : %d\n ", a+b); 
   printf("The Sub is : %d\n ", a-b); 
   printf("The Mul is : %d\n ", a*b); 
   printf("The Div is : %f\n ", b/a); 
   printf("The Mod is : %d\n ", b%a); 
   printf("The power is : %f\n", pow(a,b));
}
